		<h2 class="text-blue-900">Research</h2>

		<p>
			Research activities will be of paramount importance for the Center which will be carried out
			involving the participation of both Faculty and students of RVCE.
		</p>
		<p>The broad area of the research work will be under INTELLIGENT VIDEO ANALYTICS.</p>
		<p>
			As the country is witnessing CCTV installations in large numbers, it sets constraints to keep
			monitoring all the cameras by human operators as it is a very cumbersome process. Hence, there
			is a need to develop intelligent agents(software) which can automatically detect events and
			raise alerts/messages without much of human intervention. Since, AI, ML and DL have become the
			game changer technologies of the future, they can be very effectively applied on live camera
			feeds to get the desired results. Hence, this opens up a challenging field of research to
			develop intelligent algorithms for video analytics.
		</p>
		<p>
			Application areas will cover traffic management, public safety, law enforcement, environment,
			video forensics and health care.
		</p>
